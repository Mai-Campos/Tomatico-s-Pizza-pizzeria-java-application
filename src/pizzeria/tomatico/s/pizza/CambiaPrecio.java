     // MÃ©todo para guardar precios en un archivo
    private void guardarPrecios() {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("precios.txt"))) {
            writer.write( nuevoPrecioQueso.getText() + "\n");
            writer.write( nuevoPrecioJamon.getText() + "\n");
            writer.write( nuevoPrecioBandejaChica.getText() + "\n");
            writer.write( nuevoPrecioBandejaGrande.getText() + "\n");
            JOptionPane.showMessageDialog(this, "Precios guardados correctamente.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar los precios: " + e.getMessage());
        }
    }

