    private void aplicarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aplicarBtnActionPerformed
        
        int decision = JOptionPane.showConfirmDialog(null, "Estas seguro que deseas modificar los precios");
        
        if(decision == 0){
        pizzeria.setPrecioQueso(Integer.parseInt(nuevoPrecioQueso.getText()));
        pizzeria.setPrecioJamon(Integer.parseInt(nuevoPrecioJamon.getText()));
        pizzeria.setPrecioBandejaChica(Integer.parseInt(nuevoPrecioBandejaChica.getText()));
        pizzeria.setPrecioBandejaGrande(Integer.parseInt(nuevoPrecioBandejaGrande.getText()));
        
        guardarPrecios();
        }else{
        nuevoPrecioQueso.setText(String.valueOf(pizzeria.getPrecioQueso())); 
        nuevoPrecioJamon.setText(String.valueOf(pizzeria.getPrecioJamon())); 
        nuevoPrecioBandejaChica.setText(String.valueOf(pizzeria.getPrecioBandejaChica()));
        nuevoPrecioBandejaGrande.setText(String.valueOf(pizzeria.getPrecioBandejaGrande()));
        }
       
    }//GEN-LAST:event_aplicarBtnActionPerformed

   
   

